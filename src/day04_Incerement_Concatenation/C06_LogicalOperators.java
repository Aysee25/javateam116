package day04_Incerement_Concatenation;

public class C06_LogicalOperators {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;
        int c = 15;

        // ucunun bırbırıne esıt oldugunu kontrol et

        System.out.println( a == b && b == c);  // false

        // a sayısının pozıtıf ve 2 ıle tam bolunebılır oldugunu kontrol et

        System.out.println( a > 0 && a%2==0); // true

        // b sayısının 10 ıle 50 arasında oldugunu kontrol edın

        System.out.println(10<b && b<50); // true






    }
}
